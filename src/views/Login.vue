<template>
  <div class="ui middle aligned centered aligned grid">
    <div class="column">
      <h2 class="ui image header">
        <div class="content">Log-in to your account</div>
      </h2>
      <form class="ui large form" @submit.prevent="onLoginClicked">
        <div class="ui stacked segment">
          <div class="field">
            <div class="ui left icon input">
              <i class="user icon"></i>
              <input type="text" placeholder="Username" v-model="username" />
            </div>
          </div>
          <div class="field">
            <div class="ui left icon input">
              <i class="lock icon"></i>
              <input type="password" placeholder="Password" v-model="password" />
            </div>
          </div>
          <input type="submit" value="Login" class="ui fluid large teal submit button" />
        </div>
      </form>

      <div class="ui message">
        New to us?
        <router-link to="/register">Sign Up</router-link>
      </div>
    </div>
  </div>
</template>

<script>
import axios from "axios";
export default {
  props: {},
  data() {
    return {
      username: "",
      password: ""
    };
  },

  methods: {
    onLoginClicked() {
      axios
        .post("http://127.0.0.1:8000/authentication/token/", {
          username: this.username,
          password: this.password
        })
        .then(({ data }) => {
          localStorage.setItem("token", data.access_token);
          localStorage.setItem("refresh_token", data.refresh_token);
        });
    }
  }
};
</script>
